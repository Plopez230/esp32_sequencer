/**
 * Set default print style
 */
void seq_st7920_prepare(void) {
  u8g2.setFont(u8g2_font_4x6_tr );
}

void seq_st7920_draw_logo(void) {
  const uint8_t frame_size = 24;
  u8g2.setDrawColor(1);// Black
  u8g2.drawBox(0,0,128,64);
  u8g2.setDrawColor(0);// Black
  u8g2.setBitmapMode(false /* solid */);
  u8g2.setDrawColor(0);// Black
  u8g2.drawXBMP(32, 0, logo_width, logo_height, logo_bits);
}


/**
 * Initialize screen
 */
void seq_st7920_init(void)
{
  u8g2.begin();
  seq_st7920_prepare();
  u8g2.clearBuffer();
  seq_st7920_draw_logo();
  u8g2.sendBuffer();
}

/**
 * Refresh screen and draw
 * draw_callback calls the printing functions.
 */
void seq_st7920_draw(void (*draw_callback)())
{
  u8g2.clearBuffer();
  draw_callback();
  u8g2.sendBuffer();
}
